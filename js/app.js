// Declare or initialize a variable.
let gold = 10;

console.log(gold); // 10

// Increase the variable by 2.
gold += 2;
console.log(gold); // 12

// Multiply the variable by 2.
gold *= 2;
console.log(gold); // 24

// Increase the variable by 1.
gold++;
console.log(gold); // 25

// Decrease the variable by 1.
gold--;
console.log(gold);


